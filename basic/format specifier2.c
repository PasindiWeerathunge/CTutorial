#include <stdio.h>

int main()
{
    printf("Value :%6d\n", 85);
    printf("Value :%6d\n", -2913);

    printf("Value :%10f\n", 5.4321);
    printf("Value :%10f\n", -32.9814);

    printf("Value :%5c\n", 'A');
    printf("Value :%5c\n", 'j');

    printf("Value :%10s\n", "coding");
    printf("Value :%10s\n", "kp");
    return 0;
}
